/** 도 -> 라디안. 반 바퀴 = pi 라디안 = 180도 */
export const DEG2RAD = Math.PI / 180;

/** 한 바퀴(라디안) */
export const TAU = Math.PI * 2;

/**
 * 각도차를 [-pi, pi] 로 감는다. 유도(seeker)의 최단 회전 방향 판정에 쓴다.
 * ★ 순수 함수 — 상태도 시계도 난수도 없다 (§9.1).
 */
export function wrapAngle(a) {
  let r = a;
  while (r > Math.PI) r -= TAU;
  while (r < -Math.PI) r += TAU;
  return r;
}
